#include "ft_printf.h"

int	ft_print_sign(char sgn)
{
	if (sgn)
	{
		if (sgn == -1)
			return (ft_print_out("-", 1));
		if (sgn == 1)
			return (ft_print_out("+", 1));
	}
	return (0);
}
